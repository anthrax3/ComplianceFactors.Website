        private void SearchResult()
        {
            SystemAssignmentRules searchRule = new SystemAssignmentRules();
            searchRule.u_assignment_rules_id_pk = txtAssignmentRuleId.Text;
            searchRule.u_assignment_rules_name = txtAssignmentRuleName.Text;

            if (ddlStatus.SelectedValue == "app_ddl_all_text")
            {
                searchRule.u_assignment_rules_status_id_fk = "0";
            }
            else
            {
                searchRule.u_assignment_rules_status_id_fk = ddlStatus.SelectedValue;
            }

            try
            {
                gvsearchDetails.DataSource = SystemAssignmentRuleBLL.SearchAssignmentRule(searchRule);
                gvsearchDetails.DataBind();
            }
            catch (Exception ex)
            {
                if (ConfigurationWrapper.LogErrors == true)
                {
                    if (ex.InnerException != null)
                    {
                        Logger.WriteToErrorLog("samarmp-01.aspx", ex.Message, ex.InnerException.Message);
                    }
                    else
                    {
                        Logger.WriteToErrorLog("samarmp-01.aspx", ex.Message);
                    }
                }
            }

            if (gvsearchDetails.Rows.Count == 0)
            {
                divFooterPaging.Style.Add("display", "none");
                divHeaderPaging.Style.Add("display", "none");
            }
            else
            {
                divFooterPaging.Style.Add("display", "block");
                divHeaderPaging.Style.Add("display", "block");
            }
            if (gvsearchDetails.Rows.Count > 0)
            {
                gvsearchDetails.UseAccessibleHeader = true;
                if (gvsearchDetails.HeaderRow != null)
                {
                    //This will tell ASP.NET to render the <thead> for the header row
                    //using instead of the simple <tr>
                    gvsearchDetails.HeaderRow.TableSection = TableRowSection.TableHeader;
                }
            }
        }

        protected void btnGoSearch_Click(object sender, EventArgs e)
        {
            gvsearchDetails.PageIndex = 0;
            SearchResult();

            txtFooterPage.Text = (gvsearchDetails.PageIndex + 1).ToString();
