        protected void gvsearchDetails_RowCommand(object sender, GridViewCommandEventArgs e)
        {
            int index = Convert.ToInt32(e.CommandArgument);
            GridViewRow row = gvsearchDetails.Rows[index];

            if (e.CommandName.Equals("Select"))
            {
                if (!string.IsNullOrEmpty(Request.QueryString["page"]) && Request.QueryString["page"] == "sand" || !string.IsNullOrEmpty(Request.QueryString["page"]) && Request.QueryString["page"] == "saed")
                {
                    
                        SessionWrapper.c_location_name = row.Cells[0].Text ;
                        SessionWrapper.c_session_location_id_fk = gvsearchDetails.DataKeys[index]["c_location_system_id_pk"].ToString();
                   
                }
               
                Page.ClientScript.RegisterStartupScript(this.GetType(), "fancyboxclose", "javascript:parent.document.forms[0].submit();parent.jQuery.fancybox.close();", true);

            }


        }

