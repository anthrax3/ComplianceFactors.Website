        private void SearchResult()
        {
            try
            {

                SystemCatalog catalog = new SystemCatalog();
                if (!string.IsNullOrEmpty((string)ViewState["SearchResult"]))
                {
                    catalog.c_session_location_id_fk = txtLocationId.Text;
                    catalog.c_session_location_name = txtLocationName.Text;

                }
                else
                {
                    catalog.c_session_location_id_fk = SecurityCenter.DecryptText(Request.QueryString["id"]);
                    catalog.c_session_location_name = SecurityCenter.DecryptText(Request.QueryString["name"]);


                }

                gvsearchDetails.DataSource = SystemCatalogBLL.LocationSearch(catalog);
                gvsearchDetails.DataBind();



            }
            catch (Exception ex)
            {
                //TODO: Show user friendly error here
                //Log here
                if (ConfigurationWrapper.LogErrors == true)
                {
                    if (ex.InnerException != null)
                    {
                        Logger.WriteToErrorLog("salocsr-01.aspx", ex.Message, ex.InnerException.Message);
                    }
                    else
                    {
                        Logger.WriteToErrorLog("salocsr-01.aspx", ex.Message);
                    }
                }
            }
            if (gvsearchDetails.Rows.Count == 0)
            {

                disable_enable(false);

            }
            else
            {
                disable_enable(true);
            }
            if (gvsearchDetails.Rows.Count > 0)
            {
                gvsearchDetails.UseAccessibleHeader = true;
                if (gvsearchDetails.HeaderRow != null)
                {
                    //This will tell ASP.NET to render the <thead> for the header row
                    //using instead of the simple <tr>
                    gvsearchDetails.HeaderRow.TableSection = TableRowSection.TableHeader;
                }
            }


        }

