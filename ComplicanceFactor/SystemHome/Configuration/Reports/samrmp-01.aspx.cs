        protected void gvsearchDetails_RowCommand(object sender, GridViewCommandEventArgs e)
        {
            int rowIndex = int.Parse(e.CommandArgument.ToString());
            if (e.CommandName.Equals("Edit"))
            {
                Response.Redirect("~/SystemHome/Configuration/Reports/saerp-01.aspx?mode=edit&edit=" + SecurityCenter.EncryptText(gvsearchDetails.DataKeys[rowIndex].Values[0].ToString()), false);
            }

            else if (e.CommandName.Equals("Off"))
            {
                try
                {
                    SystemReportBLL.UpdateReportStatus(gvsearchDetails.DataKeys[rowIndex].Values[0].ToString(), false);
                }
                catch (Exception ex)
                {
                    if (ConfigurationWrapper.LogErrors == true)
                    {
                        if (ex.InnerException != null)
                        {
                            Logger.WriteToErrorLog("samrmp-01.aspx", ex.Message, ex.InnerException.Message);
                        }
                        else
                        {
                            Logger.WriteToErrorLog("samrmp-01.aspx", ex.Message);
                        }
                    }
                }
            }
            else if (e.CommandName.Equals("On"))
            {
                try
                {
                    SystemReportBLL.UpdateReportStatus(gvsearchDetails.DataKeys[rowIndex].Values[0].ToString(), true);
                }
                catch (Exception ex)
                {
                    if (ConfigurationWrapper.LogErrors == true)
                    {
                        if (ex.InnerException != null)
                        {
                            Logger.WriteToErrorLog("samrmp-01.aspx", ex.Message, ex.InnerException.Message);
                        }
                        else
                        {
                            Logger.WriteToErrorLog("samrmp-01.aspx", ex.Message);
                        }
                    }
                }
            }
            SearchResult();
        }

