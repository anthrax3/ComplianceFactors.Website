        /// <summary>
        /// Search Result
        /// </summary>
        private void SearchResult()
        {

            SystemReport report = new SystemReport();
            report.s_report_id_pk = txtReportId.Text;
            report.s_report_name = txtReportName.Text;
            if (ddlStatus.SelectedValue == "0")
            {
                report.s_report_on_off_flag = null;
            }
            else
            {
                report.s_report_on_off_flag =(ddlStatus.SelectedValue == "1"? true:false);
            }
            
            report.s_report_type_id_fk = ddlType.SelectedValue;
            //report.s_notification_type_id_fk = ddlType.SelectedValue;

            try
            {
                DataTable dtReport = new DataTable();
                dtReport = SystemReportBLL.SearchReport(report);
                dtReport.Columns.Add("s_report_on_off_flag_view", Type.GetType("System.String"));
                for (int i = 0; i < dtReport.Rows.Count; i++)
                {
                    if (dtReport.Rows[i]["s_report_on_off_flag"].ToString() == "True")
                    {
                        dtReport.Rows[i]["s_report_on_off_flag_view"] = "On";
                    }
                    else
                        dtReport.Rows[i]["s_report_on_off_flag_view"] = "Off";
                }
               
                gvsearchDetails.DataSource = dtReport;
                gvsearchDetails.DataBind();

            }
            catch (Exception ex)
            {
                //TODO: Show user friendly error here
                //Log here
                if (ConfigurationWrapper.LogErrors == true)
                {
                    if (ex.InnerException != null)
                    {
                        Logger.WriteToErrorLog("samntmp-01.aspx", ex.Message, ex.InnerException.Message);
                    }
                    else
                    {
                        Logger.WriteToErrorLog("samntmp-01.aspx", ex.Message);
                    }
                }
            }
            if (gvsearchDetails.Rows.Count == 0)
            {
                disable_enable(false);
            }
            else
            {
                disable_enable(true);
            }
            if (gvsearchDetails.Rows.Count > 0)
            {
                gvsearchDetails.UseAccessibleHeader = true;
                if (gvsearchDetails.HeaderRow != null)
                {
                    //This will tell ASP.NET to render the <thead> for the header row
                    //using instead of the simple <tr>
                    gvsearchDetails.HeaderRow.TableSection = TableRowSection.TableHeader;
                }
            }
        }
