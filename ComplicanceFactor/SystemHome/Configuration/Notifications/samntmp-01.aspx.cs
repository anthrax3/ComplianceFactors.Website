        protected void gvsearchDetails_RowDataBound(object sender, GridViewRowEventArgs e)
        {
            if (e.Row.RowType == DataControlRowType.DataRow)
            {
                Button onbutton = (Button)e.Row.FindControl("btnTurnOn");
                Button offbutton = (Button)e.Row.FindControl("btnTurnOff");

                var notification = DataBinder.Eval(e.Row.DataItem, "s_notification_on_off_flag");

                if (Convert.ToInt16(notification) == 1)
                {
                    offbutton.Style.Add("display", "inline");
                }
                else
                {
                    onbutton.Style.Add("display", "inline");
                }

