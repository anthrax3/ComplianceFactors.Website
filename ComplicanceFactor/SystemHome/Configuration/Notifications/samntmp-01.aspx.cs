        protected void gvsearchDetails_RowCommand(object sender, GridViewCommandEventArgs e)
        {
            int rowIndex = int.Parse(e.CommandArgument.ToString());
            if (e.CommandName.Equals("Edit"))
            {
                Response.Redirect("~/SystemHome/Configuration/Notifications/saent-01.aspx?edit=" + SecurityCenter.EncryptText(gvsearchDetails.DataKeys[rowIndex].Values[0].ToString()), false);
            }

            else if (e.CommandName.Equals("Off"))
            {
                try
                {
                    SystemNotificationBLL.UpdateNotificationStatus(gvsearchDetails.DataKeys[rowIndex].Values[0].ToString(),false);
                }
                catch (Exception ex)
                {
                    if (ConfigurationWrapper.LogErrors == true)
                    {
                        if (ex.InnerException != null)
                        {
                            Logger.WriteToErrorLog("samntmp-01.aspx", ex.Message, ex.InnerException.Message);
                        }
                        else
                        {
                            Logger.WriteToErrorLog("samntmp-01.aspx", ex.Message);
                        }
                    }
                }
            }
            else if (e.CommandName.Equals("On"))
            {
                try
                {
                    SystemNotificationBLL.UpdateNotificationStatus(gvsearchDetails.DataKeys[rowIndex].Values[0].ToString(),true);
                }
                catch (Exception ex)
                {
                    if (ConfigurationWrapper.LogErrors == true)
                    {
                        if (ex.InnerException != null)
                        {
                            Logger.WriteToErrorLog("samntmp-01.aspx", ex.Message, ex.InnerException.Message);
                        }
                        else
                        {
                            Logger.WriteToErrorLog("samntmp-01.aspx", ex.Message);
                        }
                    }
                }
            }
            SearchResult();
        }

