        /// <summary>
        /// Search Result
        /// </summary>
        private void SearchResult()
        {
            SystemNotification notification = new SystemNotification();
            notification.s_notification_id_pk = txtNotificationId.Text;
            notification.s_notification_name = txtNotificationName.Text;
            notification.s_notification_on_off_flag = true; //hardcoded,need to change after add dropdown for on/off
            notification.s_notification_type_id_fk = ddlType.SelectedValue;

            try
            {
                DataTable dtNotification = new DataTable();
                dtNotification = SystemNotificationBLL.SearchNotification(notification);
                dtNotification.Columns.Add("s_notification_flag", Type.GetType("System.String"));
                for (int i = 0; i < dtNotification.Rows.Count; i++)
                {
                    if (dtNotification.Rows[i]["s_notification_on_off_flag"].ToString() == "True")
                    {
                        dtNotification.Rows[i]["s_notification_flag"] = "On";
                    }
                    else
                        dtNotification.Rows[i]["s_notification_flag"] = "Off";
                }

                gvsearchDetails.DataSource = dtNotification;
                gvsearchDetails.DataBind();

            }
            catch (Exception ex)
            {
                //TODO: Show user friendly error here
                //Log here
                if (ConfigurationWrapper.LogErrors == true)
                {
                    if (ex.InnerException != null)
                    {
                        Logger.WriteToErrorLog("samntmp-01.aspx", ex.Message, ex.InnerException.Message);
                    }
                    else
                    {
                        Logger.WriteToErrorLog("samntmp-01.aspx", ex.Message);
                    }
                }
            }
            if (gvsearchDetails.Rows.Count == 0)
            {
                disable();
            }
            else
            {
                enable();
            }
            if (gvsearchDetails.Rows.Count > 0)
            {
                gvsearchDetails.UseAccessibleHeader = true;
                if (gvsearchDetails.HeaderRow != null)
                {
                    //This will tell ASP.NET to render the <thead> for the header row
                    //using instead of the simple <tr>
                    gvsearchDetails.HeaderRow.TableSection = TableRowSection.TableHeader;
                }
            }
        }
